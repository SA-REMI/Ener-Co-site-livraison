/* ---- APPLY TRANSLATIONS ---- */
function applyLang(lang) {
  const dict = I18N[lang] || I18N.fr;
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const value = dict[key];
    if (value === undefined) return;
    if (Array.isArray(value)) {
      el.innerHTML = value.map((item) => `<li>${item}</li>`).join("");
    } else if (el.hasAttribute("data-i18n-html") || /[<&]/.test(value)) {
      el.innerHTML = value;
    } else {
      el.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((el) => {
    const pairs = el.getAttribute("data-i18n-attr").split(",");
    pairs.forEach((pair) => {
      const [attr, key] = pair.split(":").map((s) => s.trim());
      if (dict[key]) el.setAttribute(attr, dict[key]);
    });
  });

  document.querySelectorAll(".lang-switch [data-lang]").forEach((btn) => {
    btn.classList.toggle("is-active", btn.dataset.lang === lang);
  });

  document.querySelectorAll(".mobile-drawer__lang button[data-lang]").forEach((btn) => {
    btn.classList.toggle("is-active", btn.dataset.lang === lang);
  });

  const currentLangLabel = document.querySelector(".lang-current");
  if (currentLangLabel) currentLangLabel.textContent = lang.toUpperCase();

  try { localStorage.setItem("enerco.lang", lang); } catch (e) {}
}

/* ---- LANG SWITCHER ---- */
function initLangSwitch() {
  const trigger = document.querySelector(".lang-trigger");
  const menu = document.querySelector(".lang-menu");
  if (trigger && menu) {
    trigger.addEventListener("click", (e) => {
      e.stopPropagation();
      menu.classList.toggle("is-open");
      trigger.setAttribute("aria-expanded", menu.classList.contains("is-open"));
    });
    document.addEventListener("click", (e) => {
      if (!menu.contains(e.target) && !trigger.contains(e.target)) {
        menu.classList.remove("is-open");
        trigger.setAttribute("aria-expanded", "false");
      }
    });
  }

  document.querySelectorAll("[data-lang]").forEach((btn) => {
    btn.addEventListener("click", () => {
      applyLang(btn.dataset.lang);
      if (menu) menu.classList.remove("is-open");
      if (trigger) trigger.setAttribute("aria-expanded", "false");
    });
  });
}

/* ---- HEADER ON SCROLL ---- */
function initHeaderScroll() {
  const header = document.querySelector(".site-header");
  if (!header) return;
  const onScroll = () => header.classList.toggle("is-scrolled", window.scrollY > 8);
  window.addEventListener("scroll", onScroll, { passive: true });
  onScroll();
}

/* ---- BURGER / MOBILE DRAWER ---- */
function initBurger() {
  const burger = document.querySelector(".burger");
  const drawer = document.querySelector(".mobile-drawer");
  const backdrop = document.querySelector(".backdrop");
  if (!burger || !drawer) return;

  const setOpen = (open) => {
    burger.setAttribute("aria-expanded", open ? "true" : "false");
    drawer.classList.toggle("is-open", open);
    if (backdrop) backdrop.classList.toggle("is-visible", open);
    document.body.classList.toggle("menu-open", open);
  };

  burger.addEventListener("click", () => {
    const open = burger.getAttribute("aria-expanded") !== "true";
    setOpen(open);
  });

  if (backdrop) backdrop.addEventListener("click", () => setOpen(false));

  // Close on link click
  drawer.querySelectorAll("a").forEach((a) => a.addEventListener("click", () => setOpen(false)));

  // Close on Escape
  document.addEventListener("keydown", (e) => {
    if (e.key === "Escape" && burger.getAttribute("aria-expanded") === "true") setOpen(false);
  });

  // Drawer lang buttons
  drawer.querySelectorAll(".mobile-drawer__lang button[data-lang]").forEach((btn) => {
    btn.addEventListener("click", () => {
      applyLang(btn.dataset.lang);
      drawer.querySelectorAll(".mobile-drawer__lang button").forEach((b) => b.classList.toggle("is-active", b === btn));
      setOpen(false);
    });
  });
}

